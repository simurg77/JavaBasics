package com.syntax.class01;

public class PrimitiveDataTypes {
	
	public static void main(String[] args) {
		/** Primative Data Types
		 * 
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * char
		 * boolean
		 */
		
		// Whole number 
		
		byte num = 10;
		
		short num1 = 100;
		
		int num2 = 1000;
		
		// l or L
		long num3 = 1212121212212l;
		
		
		// Decimal Number
		
		// f or F
		float num4 = 12.9f;
		
		double num5 = 0.888;
		
		
		// Non-Number Data Types
		
		char add = '+';
		char letter = 'a';
		
		
		// true or false (yes or no)
		
		// do not put Yes or No for value
		
		boolean hungry = true;
		
		boolean thirsty = false;
		
		
		
		
		
		
		
	}

}
